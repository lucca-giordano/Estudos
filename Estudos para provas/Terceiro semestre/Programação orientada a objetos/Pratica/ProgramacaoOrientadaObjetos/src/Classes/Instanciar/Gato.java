package Classes.Instanciar;

public class Gato {
    public String nome, raca, cor;
    public int peso, idade;

    public Gato(String nome, String raca, String cor, int peso, int idade) {
        this.nome = nome;
        this.raca = raca;
        this.cor = cor;
        this.peso = peso;
        this.idade = idade;
    }

}
